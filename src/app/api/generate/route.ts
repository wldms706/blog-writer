// 외국어(영어, 러시아어 등) 필터
function filterForeignWords(text: string): string {
  let filtered = text;
  // 1. 뷰티 영어 용어를 한국어로 치환 (삭제 대신)
  for (const [pattern, replacement] of BEAUTY_ENGLISH_REPLACEMENTS) {
    filtered = filtered.replace(pattern, replacement);
  }
  // 2. 러시아어(키릴 문자) 제거
  filtered = filtered.replace(/[а-яА-ЯёЁ]+/g, '');
  // 3. 괄호 안 영어 표기 제거 (예: "래쉬(Lash)" → "래쉬")
  filtered = filtered.replace(/\([a-zA-Z\s]+\)/g, '');
  // 4. 남은 고립된 영어 단어 제거 (한글 사이에 끼인 영어)
  filtered = filtered.replace(/(?<=[\uAC00-\uD7AF\s])[a-zA-Z]{2,}(?=[\uAC00-\uD7AF\s.,!?])/g, '');
  // 5. 연속 공백 정리
  filtered = filtered.replace(/  +/g, ' ');
  return filtered;
}


// 리스트/목록 기호 제거 ("-", "·", "•", "※" 등으로 시작하는 줄을 자연스러운 문장으로 변환)
// keepNumberedHeaders=true면 "1. 소제목" 형태의 숫자 소제목은 유지
function filterListBullets(text: string, keepNumberedHeaders: boolean = false): string {
  return text
    .split('\n')
    .map((line) => {
      let filtered = line.replace(/^\s*[-·•※]\s+/, '');
      if (!keepNumberedHeaders) {
        filtered = filtered.replace(/^\s*\d+\.\s+/, '');
      }
      return filtered;
    })
    .join('\n');
}

// 1인칭 표현 후처리 필터 (규제 업종 전용 안전장치 — 프롬프트에서 놓친 1인칭 강제 제거)
function filterFirstPerson(text: string): string {
  let filtered = text;
  // 주어 위치의 1인칭 표현 제거 (한국어는 주어 생략이 자연스러움)
  filtered = filtered.replace(/저희는\s*/g, '');
  filtered = filtered.replace(/저희\s*쪽에서는\s*/g, '');
  filtered = filtered.replace(/저희가\s*/g, '');
  filtered = filtered.replace(/저희의\s*/g, '');
  filtered = filtered.replace(/저희\s*/g, '');
  filtered = filtered.replace(/제가\s*/g, '');
  filtered = filtered.replace(/저는\s*/g, '');
  filtered = filtered.replace(/우리\s*샵/g, '');
  // 연속 공백 정리
  filtered = filtered.replace(/  +/g, ' ');
  return filtered;
}

