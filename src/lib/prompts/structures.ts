// 랜덤 선택 유틸
function pickRandom<T>(arr: T[]): T {
  return arr[Math.floor(Math.random() * arr.length)];
}

export function getRandomArticleStructure(): string {
  return pickRandom(ARTICLE_STRUCTURES).structure;
}

// 다양성 조합: 구조 + 도입부 + 톤 + 시작 시드를 랜덤 조합
