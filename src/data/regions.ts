// 블로그 지수에 따른 추천 지역 레벨
export function getRegionSuggestions(
  blogIndexLevel: string | null,
  locationCity: string,
  locationDistrict: string,
): { regions: string[]; message: string } {
  const city = locationCity?.replace(/시$|도$/, '') || '';
  const district = locationDistrict?.replace(/구$|군$/, '') || '';

  // 해당 도시/구의 동 이름 찾기
  const dongs = CITY_DONGS[district] || CITY_DONGS[city] || [];

  if (!blogIndexLevel) {
    return { regions: dongs.length > 0 ? dongs : [city], message: '블로그 지수를 설정하면 맞춤 지역을 추천해드려요' };
  }

  // 최적화 (1~3): 시/구 단위 가능
  if (blogIndexLevel.startsWith('optimal')) {
    const regions = [city];
    if (district && district !== city) regions.push(district);
    return {
      regions,
      message: '최적화 블로그! 시/구 단위 경쟁 키워드에 도전하세요',
    };
  }

  // 준최 5~7: 동 단위 + 시/구 혼합
  if (['sub7', 'sub6', 'sub5'].includes(blogIndexLevel)) {
    const regions = dongs.length > 0 ? [city, ...dongs.slice(0, 6)] : [city];
    return {
      regions,
      message: '동 단위 키워드 위주로, 간간이 시/구 키워드도 도전해보세요',
    };
  }

  // 준최 1~4: 동 단위 집중
  if (['sub4', 'sub3', 'sub2', 'sub1'].includes(blogIndexLevel)) {
    const regions = dongs.length > 0 ? dongs.slice(0, 8) : [city];
    return {
      regions,
      message: '동 단위 키워드에 집중하세요. 동+세부키워드 조합이 효과적이에요',
    };
  }

  // 일반 (sub0): 초세부
  if (blogIndexLevel === 'sub0') {
    const regions = dongs.length > 0 ? dongs.slice(0, 6) : [city];
    return {
      regions,
      message: '동+세부키워드 조합으로 틈새를 공략하세요',
    };
  }

  return { regions: dongs.length > 0 ? dongs : [city], message: '' };
}
